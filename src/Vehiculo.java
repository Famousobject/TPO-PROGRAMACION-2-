public class Vehiculo {
    private String patente;
    private String tipo;
    private int minutoIngreso;

    public Vehiculo(String patente, String tipo, int minutoIngreso) {
        this.patente = patente;
        this.tipo = tipo;
        this.minutoIngreso = minutoIngreso;
    }

    public String getPatente() {
        return patente;
    }

    public String getTipo() {
        return tipo;
    }

    public int getMinutoIngreso() {
        return minutoIngreso;
    }

    @Override
    public String toString() {
        return patente + "(" + tipo + ", min " + minutoIngreso + ")";
    }
}

